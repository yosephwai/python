# Mendefinisikan variabel
jumlah = int(input("masukan nilai jumlah andah : "))
target = 10

# Menambahkan nilai ke variabel jumlah
jumlah += 5  # Operator penugasan penambahan
print("Jumlah setelah penambahan:", jumlah)

# Menetapkan nilai berdasarkan kondisi
if jumlah >= target:
    status = "Target tercapai"
else:
    status = "Target belum tercapai"

# Menampilkan status
print("Status:", status)

#Dalam contoh ini:

#Kita menggunakan += sebagai operator penugasan penambahan untuk menambahkan 5 ke variabel jumlah.
#ita juga menggunakan operator penugasan = untuk menetapkan nilai pada variabel status berdasarkan 
 #kondisi. Hasil akhirnya dicetak ke layar.
