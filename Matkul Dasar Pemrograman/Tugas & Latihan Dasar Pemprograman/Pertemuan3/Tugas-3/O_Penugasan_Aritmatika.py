# Menggunakan operator penugasan
x = 10
print("Nilai awal x:", x)

# Penugasan
x += 5  # sama dengan x = x + 5
print("Setelah x += 5, nilai x:", x)

x -= 3  # sama dengan x = x - 3
print("Setelah x -= 3, nilai x:", x)

x *= 2  # sama dengan x = x * 2
print("Setelah x *= 2, nilai x:", x)

x /= 4  # sama dengan x = x / 4
print("Setelah x /= 4, nilai x:", x)

x %= 3  # sama dengan x = x % 3
print("Setelah x %= 3, nilai x:", x)

x **= 2  # sama dengan x = x ** 2
print("Setelah x **= 2, nilai x:", x)
