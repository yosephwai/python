# Program untuk mencetak bilangan genap dari 1 sampai 10

for i in range(2, 12, 2):  # Mulai dari 2, sampai 12 (tidak termasuk), dengan langkah 2
    print(i)  # Mencetak bilangan genap


# penjelasan 

# range(2, 12, 2):
    # 2 adalah bilangan awal (genap pertama).
    # 12 adalah batas akhir (tidak termasuk 12, sehingga berhenti di 10).
    # 2 adalah langkah increment, yang berarti kita hanya akan mendapatkan bilangan genap.
# print(i): Mencetak setiap bilangan yang dihasilkan dari loop.