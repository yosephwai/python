# Program untuk mencetak angka dari 1 hingga 15

# Inisialisasi variabel
angka = 1

# Menggunakan while untuk mencetak angka 1 hingga 15
while angka <= 15:
    print("bilangan ke- : ", angka)  # Mencetak nilai angka
    angka += 1    # Menambah angka sebanyak 1
print("Terima Kasih")


# Penjelasan
# Inisialisasi: Variabel angka diatur ke 1.
# Looping: while angka <= 15 memastikan bahwa loop akan terus berjalan sampai angka lebih besar dari 15.
# Mencetak dan Mengupdate: Di dalam loop, program mencetak nilai angka dan kemudian menambah angka dengan 1 untuk melanjutkan ke angka berikutnya.