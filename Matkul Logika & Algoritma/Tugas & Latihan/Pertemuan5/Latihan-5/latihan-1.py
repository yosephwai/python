# Program untuk menampilkan deret bilangan menggunakan perulangan for

# Deklarasi
n = int(input('Banyak Data: '))

# Menampilkan Deret Bilangan
for i in range(n):
    print(i, end=' ')


# penjelsan

# Program meminta input dari pengguna untuk menentukan banyak data yang ingin ditampilkan.
# Dengan menggunakan for i in range(n), program akan mencetak angka dari 0 hingga n-1.
# end=' ' digunakan agar output ditampilkan dalam satu baris dengan spasi sebagai pemisah