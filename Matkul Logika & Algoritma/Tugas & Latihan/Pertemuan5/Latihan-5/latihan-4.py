# Program untuk mencetak bilangan dari 10 hingga 1 (bilangan menurun)

# Inisialisasi variabel
bil = 10

# Menggunakan while untuk mencetak bilangan menurun
while bil > 0:
    print(bil, end=' ')  # Mencetak nilai bil dengan spasi di antara
    bil = bil - 1  # Mengurangi bil dengan 1

# Mencetak pesan setelah loop selesai
print("\n Mencetak Bilangan Menurun")

# Penjelasan
# Inisialisasi: Variabel bil diatur ke 10.
# Looping:
# while bil > 0: Loop ini akan terus berjalan selama bil lebih besar dari 0.
# print(bil, end=' '): Mencetak nilai bil dengan spasi tanpa membuat baris baru.
# bil = bil - 1: Mengurangi nilai bil dengan 1 untuk melanjutkan ke bilangan berikutnya.
# Pesan Akhir: Setelah loop selesai, program mencetak pesan "Mencetak Bilangan Menurun".

